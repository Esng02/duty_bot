export class TwoDates {
  first_name: string | "";
  second_name: string | "";
  first_date: string | "";
  second_date: string | "";

  
    constructor(first_name: string | "", second_name: string | "", first_date: string | "", second_date: string | "") {
      this.first_name = first_name;
      this.second_name = second_name;
      this.first_date = first_date;
      this.second_date = second_date
    }
  }